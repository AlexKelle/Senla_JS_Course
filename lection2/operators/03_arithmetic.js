/**
 * Остаток от деления (%)
 * Инкремент (++) «префиксный инкремент» ++x и «постфиксный инкремент» x ++
 * Декремент (--)
 * Унарный минус (-)
 * Унарный плюс (+)
 * Возведение в степень (**) 2 ** 3 вернёт 8.
 */

let a = 0;
a = ++a; // a + 1 => a = 1
a = a++; // a = a => a + 1

let b = 2 ** 3 // 2 - основание 3 - степень

console.log(a);
console.log(b);

